rt = int(input("Enter 1st number:"))
tr = int(input("Enter 2nd number:"))

print(rt, "+", tr, "=", rt+tr)
print(rt, "-", tr, "=", rt-tr)
print(rt, "*", tr, "=", rt*tr)
print(rt, "/", tr, "=", rt/tr)
print(rt, "%", tr, "=", rt%tr)
print(rt, "**", tr, "=", rt**tr)
print(rt, "//", tr, "=", rt//tr)


print(rt, "==", tr, "is", (rt==tr))
print(rt, "!=", tr, "is", (rt!=tr))
print(rt, ">", tr, "is", (rt>tr))
print(rt, "<", tr, "is", (rt<tr))
print(rt, ">=", tr, "is", (rt>=tr))
print(rt, "<=", tr, "is", (rt<=tr))